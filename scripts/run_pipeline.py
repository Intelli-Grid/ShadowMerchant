def normalize_category(raw: str) -> str:
    """Map a free-form scraped category string to a canonical slug."""
    if not raw:
        return "other"
    lower = raw.lower().strip()
    # Already canonical
    if lower in CANONICAL_SLUGS:
        return lower
    # Keyword prefix match
    for keyword, slug in CATEGORY_MAP.items():
        if keyword in lower:
            return slug
    return "other"


